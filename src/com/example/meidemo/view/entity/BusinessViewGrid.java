package com.example.meidemo.view.entity;

public class BusinessViewGrid {
	private String busImg;
	private String busTitle;
	private Boolean busTuanFlag;
	private float busScore;
	private String busCategory;
	private String busZone;
	private int PingLun;
	public String getBusTitle() {
		return busTitle;
	}
	public void setBusTitle(String busTitle) {
		this.busTitle = busTitle;
	}
	public Boolean getBusTuanFlag() {
		return busTuanFlag;
	}
	public void setBusTuanFlag(Boolean busTuanFlag) {
		this.busTuanFlag = busTuanFlag;
	}
	public float getBusScore() {
		return busScore;
	}
	public void setBusScore(float busScore) {
		this.busScore = busScore;
	}
	public String getBusCategory() {
		return busCategory;
	}
	public void setBusCategory(String busCategory) {
		this.busCategory = busCategory;
	}
	public String getBusZone() {
		return busZone;
	}
	
	public void setBusZone(String busZone) {
		this.busZone = busZone;
	}
	public String getBusImg() {
		return busImg;
	}
	public void setBusImg(String busImg) {
		this.busImg = busImg;
	}
	public int getPingLun() {
		return PingLun;
	}
	public void setPingLun(int pingLun) {
		PingLun = pingLun;
	}
	
	
	

}
